def add(num1, num2):
	total  = num1 + num2
	return total


def sub(num1, num2):
	total = num1 - num2
	return total

def mul(num1, num2):
	total = num1 * num2
	return total

def div(num1 ,num2):
	total = num1 / num2
	return total

def power2(num):
	return num * num


def powerof(num1, num2):
	return num1 ** num2
